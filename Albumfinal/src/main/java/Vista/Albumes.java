     public void borrarFilas() {
        int filas = modeloTabla.getRowCount();
        for (int i = 0; i < filas; i++) {
            modeloTabla.removeRow(0);
        }
        jTAlbum.setModel(modeloTabla);
    }
      public void cargaralbumes()
            
    {
        
       String consulta = "FROM Album";
       List<Album> albumes = hi.lanzarConsulta(consulta);
        System.out.println("resultado" + albumes);
       borrarFilas();
       if (!albumes.isEmpty()) {
            for (Album a : albumes) {
             
                
                //Recuperamos los campos que queremos que se muestren en la tabla.
                String titulo= a.getTitulo();
                
            
                
                Integer precio=a.getPrecio();
                
                String categoria=a.getCategoria();
                
                String nombre=a.getNumcan().getNombre();
                
                 modeloTabla.addRow(new Object[]{
                    titulo,precio,categoria,nombre
                });
                 
                jTAlbum.setModel(modeloTabla);
         
                
                
                jLAlbumes.setText(jTAlbum.getRowCount() + " Albumes encontrados");
            }
          
        } else {
      
             JOptionPane.showMessageDialog(null, "No existen datos");
        }
       
      
        
    }
    
    public void iniciarTabla() {
        //Reajustamos el modelo la tabla para que definir que tipo de dato da cada columna
        //Con esto conseguimos que funcione correctamente la reordenaci√≥n de filas por columna
        modeloTabla = new DefaultTableModel() {
            Class[] tiposDeColumna = new Class[]{
                java.lang.String.class,java.lang.String.class,java.lang.Integer.class,java.lang.String.class
               
                         };

            @Override
            public Class getColumnClass(int indiceColumna) {
                return tiposDeColumna[indiceColumna];
            }
        };
        //Damos la posibilidad a la tabla de reordenarse en funcion de la columna
        //cuando el usuario pulsa sobre la cabecera
       jTAlbum.setAutoCreateRowSorter(true);

        //Cabeceras de las columnas, mostraremos solamente el titulo, duracion, calificacion y extras.
        modeloTabla.addColumn("Titulo");
         modeloTabla.addColumn("Precio");
        modeloTabla.addColumn("Categoria");
        modeloTabla.addColumn("Nombre-cantante");
            
        jTAlbum.setModel(modeloTabla);
   
    }

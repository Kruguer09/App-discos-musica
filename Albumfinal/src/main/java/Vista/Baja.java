     public void cargarComboAlbumes() {
        DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
         
        List lista = hi.lanzarConsulta("FROM Album ");
         System.out.println("lista" + lista);
        //Comprobamos que no tengamos un objeto vacio
        if (!lista.isEmpty()) {
            //Recorremos las categorias recuperados y las vamos añadiendo al modelo
            for (Object a1 : lista) {
                Album a = (Album) a1;
                modeloCombo.addElement( String.valueOf(a.getNumalbum()) );
            }
            jCNumero.setModel(modeloCombo);
        } else {
      
            JOptionPane.showMessageDialog(this, "Error al cargar los Albumes", "Error comboDptos", 0);
        }
    }
    

    private void jBActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBActualizarActionPerformed
         if (jCNumero.getSelectedIndex() >= 0) {
            
                      
          Integer numero = Integer.parseInt(jCNumero.getSelectedItem().toString());
          Album a=new Album();
          a.setNumalbum(numero);
          a.setTitulo( jTitulo.getText());
          a.setPrecio( Integer.parseInt(jTPrecio.getText()));
          a.setCategoria(jTCategoria.getText());
          Cantante c= new Cantante();
          c.setNumcan(Integer.parseInt(jCantante.getSelectedItem().toString()) );
          a.setNumcan(c);
                  
          jCantante.getSelectedItem().toString();
          if (hi.Actualizar(a))  {     
         
            
                       JOptionPane.showMessageDialog(null, "Se ha actualizado el album correctamente");
            
                       cargarComboAlbumes();
             
                    } 
                    
          
         }
    }//GEN-LAST:event_jBActualizarActionPerformed

    private void jCNumeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCNumeroActionPerformed
        
         Album baja= new Album();
        
         if (jCNumero.getSelectedIndex() >= 0) {
                Integer numero = Integer.parseInt(jCNumero.getSelectedItem().toString());
               baja.setNumalbum(numero);
                
              baja=hi.Buscar(baja);           
         
              jTitulo.setText(baja.getTitulo());
              jTPrecio.setText(String.valueOf(baja.getPrecio()));
              jTCategoria.setText(baja.getCategoria());
            
         }
         else
             JOptionPane.showMessageDialog(this, "Error al cargar los albumes", "Error combo", 0);
        
    }//GEN-LAST:event_jCNumeroActionPerformed


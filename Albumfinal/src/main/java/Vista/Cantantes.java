    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed

          if (jTNumero.getText().length()==0 ||
            jTNombre.getText().length()==0 || jTNacionalidad.getText().length()==0
                  || jTEdad.getText().length()==0)
        {
            javax.swing.JOptionPane.showMessageDialog(null, "No han sido rellenados todos los campos");
        }
        else
        {
              
              Cantante c1=new Cantante(Integer.parseInt(jTNumero.getText()),jTNombre.getText(),
                     jTNacionalidad.getText(),Integer.parseInt(jTEdad.getText()));
          
              
              if (hi.InsertarC(c1))
            {
                javax.swing.JOptionPane.showMessageDialog(null, "Se ha registrado el alta correctamente");
            }
           else
              
              javax.swing.JOptionPane.showMessageDialog(null, "No se han introducido los datos correctamente"); 
        }
        
        
    }//GEN-LAST:event_jBGuardarActionPerformed

